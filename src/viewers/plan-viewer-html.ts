    '        kind: "bullet",',
    '        indent: bulletMatch[1].length,',
    '        text: bulletMatch[2]',
    '      });',
    '      continue;',
    '    }',
    '',
    '    if (line.trim()) {',
    '      items.push({ kind: "text", indent: 0, text: line.trim() });',
    '    }',
    '  }',
