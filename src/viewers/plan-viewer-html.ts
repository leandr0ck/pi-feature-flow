    '  el.innerHTML = el.classList.contains("checked") ? "&#10003;" : "";',
    '  el.closest(".plan-item").querySelector(".text").classList.toggle("checked");',
    '  state.modified = true;',
    '  document.getElementById("modifiedBadge").classList.add("visible");',
    '}',
    '',
    'function approvePlan() {',
    '  fetch("http://localhost:" + state.port + "/action", {',
    '    method: "POST",',
    '    headers: { "Content-Type": "application/json" },',
